$(function(){
    $(".privacyBtn").click(function(){
       $("#privacy").fadeIn(300);
       $("#privacy").addClass('blur');
       $("body").addClass('overflow');
   });

   // 팝업닫기
   $("#privacy .closeBtn").click(function(){
       $("#privacy").fadeOut(300);
       $("#privacy").removeClass('blur');
       $(".overflow").removeClass('overflow');

   });



})




